      <h1 style="text-align: center;">my top 7 shops in the DFW area</h1>
      <div class="intro">
        <h5 class="" style="padding: 30px;">This is a list compiled from dozens of competitive coffee shops around the metroplex. This list
        has also been years in the making, and though it is unfortunate every wonderful coffee place cannot be on this list, each of the seven places are carefully 
        considered to deliver a concise and fair list combining elements such as decor, menu variety and uniqueness, and of course the tast of 
        their coffee.</h5>
      </div>
        <div class="section7">
            <img src="https://floomby.io/s/031120/cGIQQM0X.jpg?download=1" id="shopimg" class="rounded mx-auto d-block" alt="Responsive image" style="margin-top: 30px;">
            <br>
            <h4 style="font-weight: bold; text-align: center;">7. Kimzey's</h4>
            <h5 class="" style="padding: 30px;">If your are in the mood for fairy tales and excellent coffee, those two worlds collide as 
                soon as you step foot in Kimzey's Coffee. The place from both inside and outside looks like something out of The Lord of the Rings, and they 
                serve amazing coffee from the lovely West Oak. Off an old country road between Dallas and Fort Worth, it is a little out of the way, but don't let that stop you from checking it out! They also have put together quite an impressive finger food menu if coffee isn't 
                your thing - <a href= "https://www.bbc.com/future/article/20201028-the-benefits-of-coffee-is-coffee-good-for-health" >though it should be</a>.
            </h5>
        <div>
        <br>
        <div class="section7">
            <img src="https://floomby.io/s/031120/Ks3WnYG6.jpg?download=1" id="shopimg" class="rounded mx-auto d-block" alt="Responsive image" style="margin-top: 30px;">
            <br>
            <h4 style="font-weight: bold; text-align: center;">6. The Wild Dectectives</h4>
            <h5 style="padding: 30px;">It might be a curious thing to include a book store on a list of the top best 
                coffee shops in the DFW metroplex, but hear me out! Opening in Febuary of 2014, The Wild detectives has always been about culture. Opening a book store and
                serving booze and coffee with it encourages people to linger, ponder, exchange ideas and get lost in the books they buy. That in it self is neat, but what secured their
                spot on this list is going above and beyond with great coffee and in-house made recipies, syrups and ingredients. The fact that they can offer a variety of 
                intrests for people to come into one place, and do them all so well, earns them a place on this list.
            </h5>
        <div>
        <br>
        <div class="section7">
            <img src="https://floomby.io/s/031120/gQARzv58.jpg?download=1" id="shopimg" class="rounded mx-auto d-block" alt="Responsive image" style="margin-top: 30px;">
            <br>
            <h4 style="font-weight: bold; text-align: center;">5. Weekend Coffee</h4>
            <h5 style="padding: 30px;">It was hard not putting Weekend Coffee closer on the list to number 1, however the accessibility of this cozy little shop 
                is one of the major draw backs. Smack-dab in the middle of downtown Dallas, inside the Joule Hotel you have this eloquent little space. Little was a word 
                aptly chosen because this place is probably about the size of your childhood bedroom, though there is seating out in the hotel lobby area close by. What puts this 
                place on the list is a combination of the ellegance of the place, and the amazing tasting coffee from Counter Culture, a fine roaster From California.
                My reccomendation is a latte with 4 shots, to taste some of their wonderful espresso, and it is something near to my heart, since it cured a caffiene headacehe
                I had there a few years back almost instantly.
            </h5>
        <div>
        <br>
        <div class="section7">
            <img src="https://floomby.io/s/031120/JUaChHqu.jpg?download=1" id="shopimg" class="rounded mx-auto d-block" alt="Responsive image" style="margin-top: 30px;">
            <br>
            <h4 style="font-weight: bold; text-align: center;">4. Mudleaf Coffee</h4>
            <h5 style="padding: 30px;">
                Situated just North of The University of Texas at Dallas In Plano is the amiable Mudleaf Coffee. The appeal to this place can be several factors,
                but it soars mainly with it's aesthetics. Despite the cold concrete floors, the deep black walls, cozy fireplace and couches provide an atmosphere that warms
                the guests almost as much as the coffee they serve. (which can be one of two roasters, Cultivar, and Crown Coffee) They also offer wonderful 
                breakfast tacos on weekends, but they sell out fast so get there early! My recommendation would be a honey lavender latte.
            </h5>
        <div>
        <br>
        <div class="section7">
            <img src="https://floomby.io/s/031120/dUErBIUd.jpg?download=1" id="shopimg" class="rounded mx-auto d-block" alt="Responsive image" style="margin-top: 30px;">
            <br>
            <h4 style="font-weight: bold; text-align: center;">3. Davis Street Espresso</h4>
            <h5 style="padding: 30px;">
                With arguably some of the best tasting espresso and competitive aesthetics, Davis Street Espresso is a treasure of the
                Bishop Arts District, and very well deserving of it's placement of this list. Serving the most widely known Oak Cliff roasters,
                They brew this to perfection, especially in their espresso based beverages. My reccomendation would be a basic cappuccino,
                although the flavors are going to be far from ordinary.
            </h5>
        <div>
        <br>
        <div class="section7">
            <img src="https://floomby.io/s/031120/yO0qSmxO.jpg?download=1" id="shopimg" class="rounded mx-auto d-block" alt="Responsive image" style="margin-top: 30px;">
            <br>
            <h4 style="font-weight: bold; text-align: center;">2. George Coffee and Provisions</h4>
            <h5 style="padding: 30px;">
                Although Kimzey's offers truly other worldly aesthetics, what is beautiful about George is it's minialistic and tasteful decor. There are few 
                places like it in the metroplex that get this right but they seem to have perfected the scandinavian minimalist look. What's more is the wonderful coffee they roast themselves and the best working enviroment imaginable with separate rooms,
                away from the hustle and bustle of the main seating area. The owners also reside upstairs so it is even more special that they have opened up their home 
                to the world to serve outstanding coffee. My reccomendation would be their chili mocha, a nice chocolate coffee drink with some heat to it.
            </h5>
        <div>
        <br>
        <div class="section7">
            <img src="https://floomby.io/s/031120/aJgubrIL.jpg?download=1" id="shopimg" class="rounded mx-auto d-block" alt="Responsive image" style="margin-top: 30px;">
            <br>
            <h4 style="font-weight: bold; text-align: center;">1. Edison Coffee Co</h4>
            <h5 style="padding: 30px;">
                Coming in at number 1 on the list is the beloved Edison Coffee Co.  Although I may be a little bias, as I proposed to my wife here, I believe there 
                are a number of reasons as to why this spot takes the cake as the best of DFW. Very rarely does a place come along that masters the art of great coffee, 
                wonderous aesthetics, and a fantastic environment for work and study quite like Edison does. Adding to that, they roast their own coffee, and it 
                is wonderful. Although it is not uncommon for coffee shops to roast their own beans, it sure is nice that Edison has their roasting operation in the 
                same place as the shop itself - really filling the place with the aroma of good things happening. Finally, one thing that makes Edison special is the staff. They have managed 
                to keep some of the same people for years and years ever since its beginning. The same people that are passionate about coffee are also passionate about finding the right drink for you. This 
                says a lot about an establishment and about the managment. My recommendation would be a pourover by any of the skilled baristas behind the bar.
            </h5>
